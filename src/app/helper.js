const helper = document.querySelector(".helper");
window.addEventListener("resize", resize);

function resize() {
  let w = window.innerWidth;

  if (w >= 1280) {
    helper.innerText = "xl";
  } else if (w >= 1000 && w < 1280) {
    helper.innerText = "lg";
  } else if (w >= 768 && w < 1000) {
    helper.innerText = "md";
  } else if (w >= 576 && w < 768) {
    helper.innerText = "sm";
  } else if (w < 576) {
    helper.innerText = "xs";
  } 
}
